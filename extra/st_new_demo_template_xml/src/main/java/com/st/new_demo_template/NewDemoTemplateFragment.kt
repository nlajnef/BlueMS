package com.st.new_demo_template

import android.os.Bundle
import android.view.LayoutInflater
import android.view.View
import android.view.ViewGroup
import androidx.fragment.app.Fragment
import androidx.fragment.app.viewModels
import com.st.core.ARG_NODE_ID
import dagger.hilt.android.AndroidEntryPoint
import com.st.new_demo_template.databinding.NewDemoTemplateFragmentBinding

@AndroidEntryPoint
class NewDemoTemplateFragment : Fragment() {

    private val viewModel: NewDemoTemplateViewModel by viewModels()
    private lateinit var binding: NewDemoTemplateFragmentBinding
    private lateinit var nodeId: String

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
    ): View {
        nodeId = arguments?.getString(ARG_NODE_ID)
            ?: throw IllegalArgumentException("Missing string $ARG_NODE_ID arguments")

        binding = NewDemoTemplateFragmentBinding.inflate(inflater, container, false)
        return binding.root
    }

    override fun onViewCreated(view: View, savedInstanceState: Bundle?) {
        super.onViewCreated(view, savedInstanceState)


    }

    override fun onResume() {
        super.onResume()
        viewModel.startDemo(nodeId = nodeId)
    }

    override fun onPause() {
        super.onPause()
        viewModel.stopDemo(nodeId = nodeId)
    }
}
